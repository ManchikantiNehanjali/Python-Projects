import turtle

# Create a turtle screen and set the background color to black
w = turtle.Screen()
w.bgcolor("black")

# Create a turtle pen named 'neha' and set its initial properties
neha = turtle.Pen()
neha.color("white")
neha.speed(0)
neha.up()
neha.setheading(180)
neha.goto(200, 0)
neha.down()

# Draw a rectangle filled with orange color
neha.begin_fill()
neha.fillcolor("orange")
neha.forward(400)
neha.setheading(0)
neha.setheading(90)
neha.forward(100)
neha.setheading(0)
neha.forward(400)
neha.setheading(270)
neha.forward(100)
neha.end_fill()

# Reset the turtle pen position and properties
neha.home()
neha.up()

# Draw a rectangle filled with green color
neha.setheading(270)
neha.goto(0, -100)
neha.setheading(0)
neha.goto(200, -100)
