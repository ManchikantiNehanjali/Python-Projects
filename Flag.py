neha.setheading(180)
neha.forward(400)
neha.setheading(90)
neha.forward(100)

# Reset turtle pen position and properties
neha.up()
neha.home()

# Draw a circle filled with blue color 
neha.down()
neha.begin_fill()
neha.circle(-50)
neha.up()
neha.goto(0, -50)
neha.down()
q = 15
neha.fillcolor("blue")
for i in range(24):
    neha.forward(50)
    neha.bk(50)
    neha.left(q)
neha.end_fill()

